                                        <p>
                                            <strong>En raison de sa position géographique, la lagune Oualidia constitue un espace d'importance écologique (nurseries naturelles pour plusieurs espèces de coquillage, parcs ostréicoles) et un lieu de refuge aux oiseaux migrateurs et sédentaires. Cette lagune est reconnue par l'Etat, à travers le Plan Directeur des Aires Protégées, comme une zone humide d'une grande importance biologique et économique et comme Site d'Intérêt Biologique et Ecologique (SIBE) de première priorité; conformément à sa certification parmi les zones humides d’importance internationale par la Convention Internationale « RAMSAR» en 2005; et par conséquent sa préservation est une nécessité absolue. Ladite convention engage le pays contractant de prendre toutes dispositions nécessaires à favoriser la conservation, la gestion, et l’utilisation rationnelle des zones humides inscrites sur la liste des SIBE.</strong>
                                        </p>
                                        <p>
                                            <strong>La lagune de Oualidia, de part son accessibilité facile, son intérêt économique pour les populations locales et sa grande importance biologique, reste un milieu potentiellement menacé par la pollution chimique et microbiologique. Les sources probables de cette pollution se résument comme suit: </strong>
                                        </p>                                        
                                        <ul class="list-angle-right">
                                            <li>
                                                Existence des habitations avoisinantes de la lagune (Infiltration des eaux usées des fosses septiques du milieu urbain et rural).
                                            </li>.
                                            <li>
                                                Pâturage des animaux à l’intérieur de la lagune pendant les périodes sèches (dépôt des déchets organiques)
                                            </li>
                                            <li>
                                                Ruissellement et lessivage des sols et des déchets organiques par la pluie à partir du versant Est, situé à une altitude de 200 mètres.
                                            </li>
                                        </ul>
                                        <p>
                                            <strong>Conscient de l’ampleur de la problématique afférente à la pollution de la lagune de Oualidia et malgré la régression substantielle de l’activité agricole, et dans le but de contribuer à la sauvegarde de la lagune, le Ministère de l’Agriculture et de la Pêche Maritime, a désigné une commission constituée des représentants de la Direction des Filières, de la Direction de l’irrigation et de l’Aménagement de l’Espace Agricole, de l’Agence pour le Développement Agricole et de l’ORMVAD pour identifier et évaluer un plan d’action à cet égard. Cette commission s’est réunie au siège de l’ORMVAD le 11/02/2010 et a effectué une visite sur le terrain le 12/02/2010 pour élaborer le plan d’action en question dont la mise en œuvre contribuerait à l’élimination de toute éventuelle source de contamination des eaux de la lagune qui serait issue des activités agricoles. La commission en question a proposé trois projets pilier II avec une évaluation physique et financière par composante et par projet.</strong>
                                        </p>
                                        <p>
                                            <strong>Les objectifs dudit plan d’action visent à : </strong>
                                        </p>
                                        <ul class="list-angle-right">
                                            <li>
                                                Eliminer toute source de contamination éventuelle d’origine  microbienne ou chimique issue des activités agricoles (pâturage au niveau de la lagune, lixiviation des nitrates et des résidus des pesticides,…).
                                            </li>
                                            <li>
                                                Contribuer à la sauvegarde environnementale de la  lagune.
                                            </li>
                                            <li>
                                                Mettre en œuvre des solutions alternatives au profit  des agriculteurs concernés.
                                            </li>
                                        </ul>
                                        <h2><u>Plan d'action :</u></h2>
                                        <p>
                                            <strong>Les projets retenus portent sur : </strong>
                                        </p>
                                        <ul class="list-angle-right">
                                            <li>
                                                L’organisation de la production laitière au niveau du Versant Est de la lagune à travers la reconversion du système actuel de production basé sur le pâturage en un système  de stabilisation à travers la production de fourrages au niveau de l’exploitation.
                                            </li>
                                            <li>
                                                L’amélioration des ressources pastorales pour le développement  des viandes rouges.
                                            </li>
                                            <li>
                                                La rationalisation de l’utilisation des intrants sur les cultures maraîchères au niveau des berges de la lagune.
                                            </li>
                                        </ul>
                                        <p>
                                            <strong>Les opérations proposées par projet sont  comme suit : </strong>
                                        </p>
                                        <h3>Projet 1 : <i>Organisation de la production laitière </i></h3>
                                        <ul class="list-angle-right">
                                            <li>
                                                Construction et équipement des centres de collecte du lait.
                                            </li>
                                            <li>
                                                Acquisition de deux ensileuses.
                                            </li>
                                            <li>
                                                Equipement des propriétés en système d’irrigation localisée.
                                            </li>
                                            <li>
                                                Acquisition d’intrants (semence de mais hybride).
                                            </li>
                                            <li>
                                                Création d’un circuit d’insémination artificielle   .
                                            </li>
                                            <li>
                                                Identification généralisée des bovins.
                                            </li>
                                            <li>
                                                Appui aux éleveurs pour le compostage du fumier.
                                            </li>
                                        </ul>
                                        <h3>Projet 2 : <i>Amélioration des ressources pastorales pour le développement  des viandes rouges </i></h3>
                                        <ul class="list-angle-right">
                                            <li>
                                                Plantation d’arbustes fourragers.
                                            </li>
                                            <li>
                                                Création de points d’eau pour l’abreuvement du cheptel.
                                            </li>
                                            <li>
                                                Appui aux éleveurs en matière d’aliments durant les deux premières années de mise en défens (6 mois par an).
                                            </li>
                                            <li>
                                                Organisation des producteurs par la création d’un groupement ANOC.
                                            </li>
                                        </ul>
                                        <h3>Projet 3 : <i>Production raisonnée des cultures maraîchères sur les berges de la lagune </i></h3>
                                        <ul class="list-angle-right">
                                            <li>
                                                Elaboration d'un fond topographique et d'un état parcellaire de la zone du projet.
                                            </li>
                                            <li>
                                                Reconversion des systèmes d’irrigation gravitaire en système d’irrigation localisée.
                                            </li>
                                            <li>
                                                Mise en place d’un observatoire de suivi de la qualité des sols et des eaux.
                                            </li>
                                            <li>
                                                Appui aux agriculteurs pour le compostage du fumier utilisé sur les cultures maraîchères.
                                            </li>
                                            <li>
                                                Mise en place d’une station de lavage des légumes.
                                            </li>
                                            <li>
                                                Organisation des producteurs et appui logistique.
                                            </li>
                                        </ul>
